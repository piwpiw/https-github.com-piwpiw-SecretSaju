import { chromium } from "playwright";

const base='http://127.0.0.1:3002';
const browser=await chromium.launch({headless:true, channel:'msedge'});
const page=await browser.newPage();
await page.goto(base+'/login');
const forms = await page.locator('form').all();
console.log('forms', forms.length);
const buttons = await page.$$eval('button', els => els.map(el=>({text: (el.textContent||'').trim(), type: el.type, disabled: el.disabled }))); 
console.log('buttons', JSON.stringify(buttons.filter(b=>b.text).slice(0,25), null,2));
const inputs = await page.$$eval('input', els => els.map(el=>({type:el.type, placeholder:el.placeholder, name:el.name, value:el.value}))); 
console.log('inputs', JSON.stringify(inputs, null,2));
await page.locator('input[type="email"]').first().fill('piwpiw@naver.com');
await page.locator('input[type="password"]').first().fill('admin');
await page.getByRole('button',{name:/이메일로 로그인|로그인/}).first().click();
await page.waitForTimeout(5000);
console.log('url', page.url());
console.log('path', new URL(page.url()).pathname);
const text = await page.locator('body').innerText();
console.log('has 홈', text.includes('홈')); 
console.log('has 로그인', text.includes('로그인')); 
console.log('has 비밀번호', text.includes('비밀번호')); 
await page.screenshot({path:'logs/login-manual.png', fullPage:true});
await browser.close();
