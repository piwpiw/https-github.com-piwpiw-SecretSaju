import { chromium } from "playwright";

const base='http://127.0.0.1:3002';
const browser=await chromium.launch({ headless:true, channel:'msedge' });
const page=await browser.newPage();
await page.goto(base+'/',{waitUntil:'domcontentloaded'});
await page.waitForTimeout(1000);
const form = page.locator('form').filter({ has: page.locator('input[placeholder="홍길동"]')}).first();
await form.locator('input[placeholder="홍길동"]').fill('HOMEUSER');
await form.locator('input[placeholder="예: 1990 (또는 19900101)"]').fill('1990');
await form.locator('input[placeholder="1~12"]').fill('1');
await form.locator('input[placeholder="1~31"]').fill('1');
await form.locator('select').first().selectOption('12');
await form.locator('select').nth(1).selectOption('00');
await form.getByRole('button',{name:'사주 분석 시작'}).click();
await page.waitForTimeout(7000);
console.log('url', page.url());
console.log('contains official', await page.locator('text=공식 사주 분석 리포트').count());
console.log('contains loading', await page.locator('text=사주 분석 중...').count());
console.log('contains flow title', await page.locator('text=출생정보 입력').count());
console.log('contains 사주 분석 시작', await page.locator('text=사주 분석 시작').count());
console.log('body contains 결과', (await page.locator('body').innerText()).includes('사주 분석')); 
await page.screenshot({ path:'logs/home-form-submit.png', fullPage:true});
await browser.close();
